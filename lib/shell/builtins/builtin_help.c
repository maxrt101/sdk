#include "shell/shell.h"
#include "log/log.h"

/* Defines ================================================================== */
#define LOG_TAG shell

/* Macros =================================================================== */
/* Exposed macros =========================================================== */
/* Enums ==================================================================== */
/* Types ==================================================================== */
/* Variables ================================================================ */
/* Private functions ======================================================== */
/* Shared functions ========================================================= */
int8_t builtin_help(shell_t * sh, uint8_t argc, const char ** argv) {
  log_printf("Available commands:\r\n");

  SHELL_ITER_COMMANDS(cmd) {
    log_printf("%s - %s\r\n", cmd->name, cmd->help);
  }

  return SHELL_OK;
}
