int8_t builtin_echo(shell_t * sh, uint8_t argc, const char ** argv) {
  for (uint8_t i = 1; i < argc; ++i) {
    log_printf("%s ", argv[i]);
  }
  log_printf("\r\n");
  return SHELL_OK;
}
