#include "time/time.h"

/* Defines ================================================================== */
/* Macros =================================================================== */
/* Enums ==================================================================== */
/* Types ==================================================================== */
/* Variables ================================================================ */
static milliseconds_t global_time = 0;

/* Private functions ======================================================== */
/* Shared functions ========================================================= */
void runtime_inc(milliseconds_t ms) {
  global_time += ms;
}

milliseconds_t runtime_get() {
  return global_time;
}

void runtime_set(milliseconds_t ms) {
  global_time = ms;
}
